import express from "express";
import { asyncHandler } from "../../../../../middlewares/asyncHandler.js";
import UpdatePaymentMethod from "../../../controllers/PaymentMethod/UpdatePaymentMethod.controller.js";
const router = express.Router();
/**
 * @swagger
 * /payment-method:
 *   put:
 *     summary: Update Payment Method
 *     tags: [PaymentMethods]
 *     security:
 *       - BearerAuth: []
 *     parameters:
 *       - in: query
 *         name: id
 *         required: true
 *         schema:
 *           type: string
 *           example: "6535c90b3d7b3110acb4d24c"
 *         description: The Payment Method ID to update
 *     requestBody:
 *       required: true
 *       content:
 *         application/json:
 *           schema:
 *             $ref: '#/components/schemas/PaymentMethod'
 *     responses:
 *       200:
 *         description: Payment Method updated successfully
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 success: { type: boolean }
 *                 message: { type: string }
 *                 data:
 *                   $ref: '#/components/schemas/PaymentMethod'
 *       400:
 *         description: Bad request
 *       401:
 *         description: Unauthorized
 *       404:
 *         description: Not found
 *       500:
 *         description: Internal server error
 */

router.put("/", asyncHandler(UpdatePaymentMethod));
export default router;
